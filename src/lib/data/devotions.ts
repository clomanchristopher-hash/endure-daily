export function getDevotionById(id: string): Devotion | undefined {
  return devotions.find((d) => d.id === id);
}

// Deterministic daily rotation so "today's devotion" is stable across visits
// but changes day to day. Swappable for a real scheduling table later.
export function getDevotionForDate(date: Date, list: Devotion[] = devotions): Devotion {
  const start = Date.UTC(2026, 0, 1);
  const day = Math.floor((Date.UTC(date.getFullYear(), date.getMonth(), date.getDate()) - start) / 86400000);
  const index = ((day % list.length) + list.length) % list.length;
  return list[index];
}
